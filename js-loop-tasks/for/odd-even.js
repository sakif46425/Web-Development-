//subtask-1: find all odd numbers from 61 to 100

console.log("Odd numbers between 61 to 100: ");
for (let num = 61; num <= 100; num++)
{
    if (num % 2 !== 0)
    {
        console.log(num);
    }
}

console.log("Even numbers between  78 and 98 :");
for (let num = 78; num <=98; num++)
{
    if (num % 2 === 0)
    {
        console.log(num);
    }
}